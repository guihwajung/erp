         this.fnGrid_ExpandUp = function(obj, e) {

        	var colnm = this.gfnGridGetBindColumnNameByIndex(obj, e.cell);

        	if(colnm == "BTN_ATFL") {
        		var fileManager = {};
        		fileManager.CD_GUBUN = "DWR1";
        		fileManager.CD_DIR = [this.dsList.getColumn(e.row, "CD_REFVAL")];

        		if(this.FormInfo.TY_AUTH == "R") {
        			fileManager.IS_READONLY = true;
        		} else {
        			fileManager.IS_READONLY = false;
        		}
        		this.gfnFileManager(fileManager, "fnFileCallback");
        	}
        }

        this.fnFileCallback = function(strID, val) {
        	// val.IsChange : 변경여부, val.Cnt : 파일개수

        	// 변경시 조회를 다시 하거나
        	if(val.IsChange == true) {
        		this.FormBtns.Select.click();
        	}

        	// 파일개수를 다시 셋팅
        	this.dsList.set_enableevent(false);
        	if(val.Cnt == 0) {
        		this.dsList.setColumn(this.dsList.rowposition, "BTN_ATFL", "첨부");
        	} else {
        		this.dsList.setColumn(this.dsList.rowposition, "BTN_ATFL", "첨부(" + val.Cnt + ")");
        	}
        	this.dsList.set_enableevent(true);
        };

        this.fnGrid_EnterCell = function(obj, row, cell) {
        	var colnm = this.gfnGridGetBindColumnNameByIndex(this.dxGrid, cell);

        	if(this.dsList.getColumn(row, "YN_EDIT") == "N") {
        		return false;
        	}
        }
